package fundamentos;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		
		// Operadores: == != >= > <= <
		
		//Igualdade (==)
		
		int num1 = 97;
		int num2 = 'a';
		
		System.out.println(num1 == num2);
		//Com a converão implicita o que está sendo comparado é
		//97 == 97
		
		//Diferença (!=)
		
		System.out.println(3 != 2);
		
		//Maior ou igual (>=)
		
		System.out.println(3 >= 2);
		
		//Maior que (>)
		
		System.out.println(3 > 2);
		
		//Menor ou igual
		
		System.out.println(3 <= 3);
		
		//Menor que
		
		System.out.println(3 < 4);
		
		
	}
	
}
