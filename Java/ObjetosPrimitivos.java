package fundamentos;

public class ObjetosPrimitivos {
	
	public static void main(String[] args) {
		//Objetos primitivos (Wrappers)
		// São as versões orientadas a objetos dos tipos primitivos
		
		//Char
		//Declaração
		Character a = '!';
		
		//Boolean
		Boolean bo;
		//Declaração
		bo = true;
		//ou
		bo = false;
		
		//byte
		Byte c;
		//Declaração
		c = 2;
		
		//int
		Integer d = 23;
		
		//long
		Long e = 234L;
		
		
		//float
		Float f = 5.1f;
		
		//double
		Double g = 6.1;
			
		/**
		 * Este processo é feito já que as versões dos wrappers 
		 * são versões mais ricas dos tipos primitivos
		 * O que muda em sua declaração é a letra maiuscula e 
		 * em alguns casos o nome se tornou completo, como exemplo:
		 * char passou a ser Character
		 */
	}

}
